nota1 = float(input("Insira a PRIMEIRA nota\n"))
nota2 = float(input("Insira a SEGUNDA nota\n"))
nota3 = float(input("Insira a TERCEIRA nota\n"))
nota4 = float(input("Insira a QUARTA nota\n"))

